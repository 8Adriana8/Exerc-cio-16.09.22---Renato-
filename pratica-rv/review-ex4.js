/*4. Dado os três lados de um triângulo determinar o perímetro do mesmo.*/

const ladoTrianguloA = Number(prompt('Informe o lado A do triângulo.'))
const ladoTrianguloB = Number(prompt('Informe o lado B do triângulo.'))
const ladoTrianguloC = Number(prompt('Informe o lado C do triângulo.'))

const perimetroTriangulo = ladoTrianguloA + ladoTrianguloB + ladoTrianguloC

alert('O perímetro do triangulo é ' + perimetroTriangulo)